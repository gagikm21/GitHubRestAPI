package com.example.githubrestapi.model;

public class Week {

    private String w;
    private int a;
    private int d;
    private int c;

    public String getW() {
        return w;
    }

    public void setW(String w) {
        this.w = w;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
}
